def jitter(name, base, spread=0.006):
    h = int(hashlib.md5(name.encode()).hexdigest(),16)
    dlat = ((h % 1000)/1000 - 0.5)*spread
    dlon = (((h//1000)%1000)/1000 - 0.5)*spread
    return base[0]+dlat, base[1]+dlon

bars=[]
seen=set()
for row in B:
    name=row[0]; nb=row[1]; cat=row[2]
    key=name.lower().strip()
    if "skip" in key or key in seen: 
        continue
    seen.add(key)
    if len(row)>=5:
        lat,lon=row[3],row[4]
    else:
        lat,lon=jitter(name, N[nb])
    bars.append({"id":len(bars)+1,"name":name,"neighborhood":nb,"category":cat,
                 "lat":round(lat,5),"lon":round(lon,5)})

